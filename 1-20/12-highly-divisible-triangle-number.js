/*

The sequence of triangle numbers is generated by adding the natural numbers. So the 7th triangle number would be 1 + 2 + 3 + 4 + 5 + 6 + 7 = 28. The first ten terms would be:

1, 3, 6, 10, 15, 21, 28, 36, 45, 55, ...

Let us list the factors of the first seven triangle numbers:

 1: 1
 3: 1,3
 6: 1,2,3,6
10: 1,2,5,10
15: 1,3,5,15
21: 1,3,7,21
28: 1,2,4,7,14,28
We can see that 28 is the first triangle number to have over five divisors.

What is the value of the first triangle number to have over five hundred divisors?

*/

const log = console.log;

let startingNumber = 1;
let nextTriangleNumber = 2;
let numberOfDivisors = 1;

function clean() {
  console.time('Completion time')
  for(let i = 0; numberOfDivisors < 500; i++) {
    numberOfDivisors = 1;
    divisorsTest(startingNumber)
    log(`number: ${startingNumber}, divisors: ${numberOfDivisors}`);
    startingNumber += nextTriangleNumber;
    nextTriangleNumber += 1;
  }
  log(startingNumber);
  console.timeEnd('Completion time');
}

function divisorsTest(numberToTest) {
  for (let i = 0; i < numberToTest; i ++) {
    if (numberToTest % i === 0) {
      numberOfDivisors += 1;
    }
  }
}

clean();
